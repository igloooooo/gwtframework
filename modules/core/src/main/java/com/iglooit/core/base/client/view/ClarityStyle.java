    private interface CSSStyle
    {
        String getCss();
    }

    public enum WizardStyle implements CSSStyle
    {
        WIZARD_PANEL("wizard-panel"),
        BUTTON_BAR("button-bar");

        private String css;

        WizardStyle(String css)
        {
            this.css = css;
        }

        @Override
        public String getCss()
        {
            return css;
        }
    }

    public enum ProductStyle implements CSSStyle
    {
        PRODUCT_SUMMARY("product-summary"),
        PRODUCT_CONTAINER("product-container"),
        PRODUCT_PARENT("product-parent");

        private String css;

        ProductStyle(String css)
        {
            this.css = css;
        }

        @Override
        public String getCss()
        {
            return css;
        }
    }

    // event standard colour styles (e.g. for alarms, performance, etc)
    public enum EventStyle implements CSSStyle
    {
        RED("event-red"),
        ORANGE("event-orange"),
        YELLOW("event-yellow"),
        PURPLE("event-purple"),
        INDIGO("event-indigo"),
        PINK("event-pink"),
        BLUE("event-blue"),
        GREEN("event-green"),
        GRAY("event-gray");

        private String css;

        EventStyle(String css)
        {
            this.css = css;
        }

        @Override
        public String getCss()
        {
            return css;
        }
    }

    /**
     * example usage:      ClarityStyle.asCSSClass(ClarityStyle.ProductStyle.PRODUCT_SUMMARY);
     *
     * @param t a Css typed enum
     * @return the String representing this CSSClass.
     */
    public static String asCSSClass(CSSStyle t)
    {
        return asCSSClass(t.getCss());
    }

    public static String asCSSClass(String className)
    {
        return "." + className;
    }

    public static String asCSSId(String idName)
    {
        return "#" + idName;
    }
